return {
  'ibhagwan/fzf-lua',
  -- optional for icon support
  dependencies = { 'nvim-tree/nvim-web-devicons' },
  config = function()
    -- calling `setup` is optional for customization
    require('fzf-lua').setup {
      winopts = { preview = { layout = 'vertical', vertical = 'down:65%' } },
      lsp = {
        async_or_timeout = true,
      },
      -- diagnostics = {
      --   winopts = { preview = { layout = 'vertical' } },
      -- },
      -- files = {
      --   winopts = { preview = { layout = 'vertical', vertical = 'down:65%' } },
      -- },
      live_grep_native = {},
    }

    local fzf = require 'fzf-lua'

    vim.keymap.set('n', '<leader>sh', fzf.helptags, { desc = '[S]earch [H]elp' })
    vim.keymap.set('n', '<leader>sk', fzf.keymaps, { desc = '[S]earch [K]eymaps' })
    vim.keymap.set('n', '<leader>sf', fzf.files, { desc = '[S]earch [F]iles' })
    vim.keymap.set('n', '<leader>sb', fzf.builtin, { desc = '[S]earch [B]uiltin commands (fzf-lua)' })
    vim.keymap.set('n', '<leader>sw', fzf.grep_cword, { desc = '[S]earch current [W]ord' })
    vim.keymap.set('n', '<leader>sg', fzf.live_grep_native, { desc = '[S]earch by [G]rep' })
    vim.keymap.set('n', '<leader>sd', fzf.diagnostics_document, { desc = '[S]earch [D]iagnostics' })
    vim.keymap.set('n', '<leader>sr', fzf.resume, { desc = '[S]earch [R]esume' })
    vim.keymap.set('n', '<leader>s.', fzf.oldfiles, { desc = '[S]earch Recent Files ("." for repeat)' })
    vim.keymap.set('n', '<leader><leader>', fzf.buffers, { desc = '[ ] Find existing buffers' })
    vim.keymap.set('n', '<leader>/', fzf.lgrep_curbuf, { desc = '[/] Fuzzily search in current buffer' })
    vim.keymap.set({ 'n', 'v', 'i' }, '<C-x><C-f>', function()
      require('fzf-lua').complete_path()
    end, { silent = true, desc = 'Fuzzy complete path' })

    fzf.register_ui_select()
  end,
}
